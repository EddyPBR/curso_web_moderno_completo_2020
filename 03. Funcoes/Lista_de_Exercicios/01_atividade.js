/**
 * Crie uma função que dado dois valores (passados como parâmetros) mostre no console a soma, 
 * subtração, multiplicação e divisão desses valores
 */

const atividade01 = (val1, val2) => {
  console.log(`${val1} + ${val2} = ${val1 + val2}`);
  console.log(`${val1} - ${val2} = ${val1 - val2}`);
  console.log(`${val1} * ${val2} = ${val1 * val2}`);
  console.log(`${val1} / ${val2} = ${val1 / val2}`);
}

atividade01(10, 2);
atividade01(5, 2);
